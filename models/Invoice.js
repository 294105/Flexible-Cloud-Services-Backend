const mongoose = require('mongoose');

const invoiceSchema = new mongoose.Schema({
  purchaseOrderId: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'PurchaseOrder',
    required: true
  },
  clientDetails: {
    name: String,
    email: String,
    phone: String,
    address: String
  },
  invoiceDate: {
    type: Date,
    default: Date.now
  },
  dueDate: Date
}, { timestamps: true });

const Invoice = mongoose.model('Invoice', invoiceSchema);
module.exports = Invoice;
